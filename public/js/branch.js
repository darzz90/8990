$(document).ready(function(){
    $.ajaxSetup({
        headers: {
            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
        }
    });
    autogeneratedCodeBranch();
});

function autogeneratedCodeBranch() {
    $.ajax({
        url: "/branchCode",
        type: "post",
        async: false,
        success: function (data) {
            $("#branch_code").val(data);
        }
    });
}

